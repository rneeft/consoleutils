namespace System
{
    /// <summary>
    /// Specifies Whether or not to use a NewLine
    /// </summary>
    public enum NewLine
    {
        /// <summary>
        /// Indicated to use <see cref="Console.WriteLine"/>
        /// </summary>
        Yes,

        /// <summary>
        /// Indicated to use <see cref="Console.Write"/>
        /// </summary>
        No
    }
}